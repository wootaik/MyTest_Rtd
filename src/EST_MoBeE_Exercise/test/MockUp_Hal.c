uint16_t Adc_GetData(uint16_t channel)
{
	if(channel == SUPPLY_VOL_CHANNEL){
		return Hal_SupplyVoltage;
	}else if (channel == MOTOR_CUR_CHANNEL){
		return Hal_MotorCurrent;
	}else{
		return 0;
	}

}



uint8_t Can_GetData(uint16_t signal)
{        

  return Hal_MsbCommand;
}

void Can_SetData(uint16_t signal)
{
  Hal_MsbStateMessage = signal;
}


void Pwm_SetData(uint16_t motor_duty)
{

  Hal_MotorDuty = motor_duty;

}



void Dio_SetMotorEnable(uint8_t motor_enable)
{

  Hal_MotorEnable = motor_enable;

}
  
     
void Dio_SetMotorDirection(uint8_t motor_dir)
{

  Hal_MotorDirection = motor_dir;

}




